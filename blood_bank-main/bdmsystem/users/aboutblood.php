        <h6 class="sub-title">Blood Components</h6>
        <p>Plasma – About 55% of whole blood is, a fluid that is the blood’s liquid medium, which by itself is straw-yellow in color. It has protein components called albumin, globulin and fibrinogen. Broadly speaking, albumin maintains the structural balance of blood, globulin builds resistance to bacterial infections, fibrinogen helps blood coagulate.</p>

        <p>Red Cells carry oxygen from the lungs to various body tissues and take back carbon dioxide from the cells and tissues which the body gets rid of as exhaled air. The basic substance of red cells is iron and protein, known as haemoglobin. The haemoglobin count is an indicator of the health of blood. On an average, a healthy male should have 14 – 16 gm per 100 millilitre and in a female around 12 – 14 gm per 100 millilitre.</p>

        <p>White cells act mainly as body scavengers and guards. They help in the immune system of the body and act as defense forces of the body, killing bacteria or any other organisms entering the body.</p>

        <p>Platelets help in the clotting and coagulation of blood. They also repair the tiny blood vessels in the body which crack under pressure, thereby preventing haemorrhages under the skin.</p>
