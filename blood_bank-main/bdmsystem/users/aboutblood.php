        <h6 class="sub-title">Blood Groups</h6>
        <p>Although all blood is made of the same basic elements, not all blood is alike. In fact, there are eight different common blood types, which are determined by the presence or absence of certain antigens substances that can trigger an immune response if they are foreign to the body. Since some antigens can trigger a patient’s immune system to attack the transfused blood, safe blood transfusions depend on careful blood typing and cross-matching.</p>

        <p>There are four major blood groups determined by the presence or absence of two antigens A and B on the surface of red blood cells:</p>

        <p><h6 style="color:#400082;">Group A</h6> has only the A antigen on red cells (and B antibody in the plasma)</p>

        <p><h6 style="color:#400082;">Group B</h6> has only the B antigen on red cells (and A antibody in the plasma)</p>

        <p><h6 style="color:#400082;">Group AB</h6> has both A and B antigens on red cells (but neither A nor B antibody in the plasma)</p>

        <p><h6 style="color:#400082;">Group O</h6> has neither A nor B antigens on red cells (but both A and B antibody are in the plasma)</p>

        <p>
        In addition to the A and B antigens, there is a third antigen called the Rh factor, which can be either present (+) or absent. In general, Rh negative blood is given to Rh-negative patients, and Rh positive blood or Rh negative blood may be given to Rh positive patients.</p>

        <p> The universal red cell donor has Type O negative blood type.<br>
